function solve (num1, num2, opertator) {
    switch (opertator) {
        case '+':
            console.log(num1 + num2)
            break;
            case '-':
                console.log(num1 - num2)
                break;
                case '*':
                    console.log(num1 * num2)
                    break;
                    case '/':
                        console.log(num1 / num2)
                        break;
                        case '%':
                            console.log(num1 % num2)
                            break;
                            case '**':
                                console.log(num1 ** num2)
                                break;

    }
}
